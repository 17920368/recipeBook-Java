public class Postre {
    String nombre;
    String i1;
    String i2;
    String i3;

    public Postre(String nombre, String i1, String i2, String i3) {
        this.nombre = nombre;
        this.i1 = i1;
        this.i2 = i2;
        this.i3 = i3;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getI1() {
        return i1;
    }

    public void setI1(String i1) {
        this.i1 = i1;
    }

    public String getI2() {
        return i2;
    }

    public void setI2(String i2) {
        this.i2 = i2;
    }

    public String getI3() {
        return i3;
    }

    public void setI3(String i3) {
        this.i3 = i3;
    }
    

    
}
